package Tiles.Structs;

public class Position {
    int x;
    int y;

    public Position( int x, int y){
        this.x = x;
        this.y = y;
    }

    public void setX (int newX){ x= newX;}
    public void getY (int newY){ y= newY;}

    public int getX (){ return x;}
    public int getY (){ return y;}





}
